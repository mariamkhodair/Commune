export const translations: Record<Lang, typeof en> = { en, ar };

export function tr(
  lang: Lang,
  key: keyof typeof en,
  vars?: Record<string, string | number>
): string {
  const val = translations[lang][key] ?? translations.en[key] ?? key;
  if (!vars) return val;
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replace(`{${k}}`, String(v)),
    val
  );
}

