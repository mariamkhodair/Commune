export function tr(
  dict: typeof en,
  key: keyof typeof en,
  vars?: Record<string, string | number>
): string {
  let str = dict[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}

